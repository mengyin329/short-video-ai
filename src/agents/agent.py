def build_system_prompt() -> str:
    """
    构建系统提示词
    
    根据配置文件中的sp字段决定使用哪种提示词：
    - 如果sp字段是占位符或用户想使用内置提示词，则使用内置的智能路由提示词
    - 如果sp字段有自定义内容，则使用sp字段的内容
    """
    workspace_path = os.getenv("COZE_WORKSPACE_PATH", "/workspace/projects")
    config_path = os.path.join(workspace_path, LLM_CONFIG)
    
    try:
        with open(config_path, 'r', encoding='utf-8') as f:
            cfg = json.load(f)
        
        sp_content = cfg.get("sp", "")
        
        # 如果配置文件中有自定义sp且不是占位符，则使用
        if sp_content and sp_content.strip() and not sp_content.strip().startswith("你是短视频AI编剧助手"):
            return sp_content
    except Exception:
        pass
    
    # 使用内置的智能路由提示词
    return _build_intelligent_routing_prompt()


