def build_agent(ctx=None, user_config: Optional[dict] = None):
    """
    构建Agent实例
    
    Args:
        ctx: 运行时上下文（用于请求追踪）
        user_config: 用户自定义配置，支持以下字段：
            - api_key: 用户的 API Key
            - base_url: 用户的 Base URL（模型服务地址）
            - model: 用户想使用的模型名称
            - temperature: 温度参数（可选）
    
    Returns:
        Agent实例
    """
    workspace_path = os.getenv("COZE_WORKSPACE_PATH", "/workspace/projects")
    config_path = os.path.join(workspace_path, LLM_CONFIG)
    
    # 加载默认配置
    with open(config_path, 'r', encoding='utf-8') as f:
        cfg = json.load(f)
    
    # 确定使用哪个配置（用户配置优先）
    if user_config:
        # 用户自定义模式：使用用户提供的配置
        api_key = user_config.get("api_key")
        base_url = user_config.get("base_url")
        model = user_config.get("model", cfg['config'].get("model"))
        temperature = user_config.get("temperature", cfg['config'].get('temperature', 0.7))
    else:
        # 默认模式：使用系统配置
        api_key = os.getenv("COZE_WORKLOAD_IDENTITY_API_KEY")
        base_url = os.getenv("COZE_INTEGRATION_MODEL_BASE_URL")
        model = cfg['config'].get("model")
        temperature = cfg['config'].get('temperature', 0.7)
    
    # 构建系统提示词
    system_prompt = build_system_prompt()
    
    # 创建LLM实例
    llm = ChatOpenAI(
        model=model,
        api_key=api_key,
        base_url=base_url,
        temperature=temperature,
        streaming=True,
        timeout=cfg['config'].get('timeout', 600),
        extra_body={
            "thinking": {
                "type": cfg['config'].get('thinking', 'disabled')
            }
        } if not user_config else {},  # 用户自定义模式不添加 extra_body
        default_headers=default_headers(ctx) if ctx and not user_config else {}
    )
    
    # 创建并返回Agent
    return create_agent(
        model=llm,
        system_prompt=system_prompt,
        tools=STORYBOARD_TOOLS + DESIGN_TOOLS,
        checkpointer=get_memory_saver(),
        state_schema=AgentState,
    )
